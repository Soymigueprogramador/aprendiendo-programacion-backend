// Instalaciones locales vs globales.

/*
    Las instalaciones globales se aplican sobre todo el dispositivo que usamos para desarrollar mientras que las instalaciones locales se aplican
    sobre un proyecto desarrollado espesificamente.
*/

// Bentajas de las instalaciones globales y locales.

/*
    Se libera de trabajar con dependencias , es decir que las mismas se instalan una sola vez y listo
*/

// Desbentajas de las instalaciones globales y locales.

/*
    Se dejan las dependencias sin actualizar o directamente quedan obsoletas rapidamente. 
*/


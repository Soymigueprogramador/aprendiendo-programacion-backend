// ¿ Que es Node.js ?

/*
    Node.js es un entorno de ejecucion de JavaScript, nos permite ejecutar codigo por fuera del navegador. 
*/

// Caracteristicas de Node.js.

/*¡
    Las caracteristicas de Node,js son:
    * Trabaja con el motor V8 que es el mismo motor de google chrome. 
    * Se actualiza al mismo ritmo que JavaScript. 
    * Tiene un enfoque hacia la programacion asyncronica.
    * Es de codigo abierto. 
*/

// ¿ Que podemos hacer con Node.js ?

/*
    Node.js nos permite generar aplicacions del lado del Back-end.
*/

// Iniciando un proyecto con Node.js.

/**
    Pasos para crear un nuevo proyecto.
    * Creamo una carpeta donde vamos a guardar nuestro proyecto.
    * Abrimos una nueva terminal ( cualquier terminal )
    * Usamos el comando ' npm init '.
    * Llenamos el formulario con los datos que nos va pidiendo.
    * Damos enter para confirmar que todo este bien y listo.
    Al crear el proyecto se nos genera automaticamente el package.json con la informacion que es necesaria para el proyecto.
*/

// ¿ Como organizar las carpetas del proyecto ?
/** 
    Pasos para organizar las carpetas del proyecto.
    * Creamos una carpeta llamada ' src '
    * Dentro de la carpeta src creamos el punto de ingreso que va a levantar a nuestro proyecto ' app.js '
*/

// Aclaraciones:
/**
    * Cpm el comando ' npm init --y ' nos ahorramos el proceso ants mencionado.
    * El archivo package.json es editable y su modificacion no afecta al proyecto.
*/
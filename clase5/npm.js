// Modulos de terceros.

/*
    Los modulos de terceros son paquetes que podemos instalar en nuestra app y estos nos ayudaran con tareas espesiicas.
*/

// ¿ Que es un paquete ?

/*
    Un paquete es un conjunto de modulos que se usa para resolver una tarea en particulas.
    Se puede identificar a un paquete porque trae un package.json. 
*/

// Pasos para instalar un paquete.
/**
    Los pasos son:
    * npm install nombreDElAdEPENDENCIAopAQUETE
    * npm i nombreDElAdEPENDENCIAopAQUETE
*/

// ¿ Que es una dependencia ?

/*
    Una dependencia es un paquete que mi app necesita para poder funcionar.
*/

// ¿ Como desinstalar una dependencia ?

/**
    Para desinstalar una dependencia hacemos esto:
    * npm uninstall NOMBREdElAdEPENDENCIA.
*/

// ¿ Como listar y actualizar una dependencia ?

/*
    Para listar las dependencias usamos este comando 
    npm outdated
    Para actualizar todas las dependencias usamos este comando 
    npm update
*/

// ¿ Que son las dependencias de desarrollo ?

/*
    Dentro de lo que es la creacion de una aplicacion, software o sistema tenemos 2 etapas.
    1- Desarrollo
    2- Produccion.

    Las dependencias de desarrollo son herramientas que nos van a ayudar a desarrollar nuestra app pero a su vez esas herramientas
    no son indispensables para que nuestra app funcione en la etapa de produccion. 

    Para instalar una dependencia como dependencia de desarrollo usamos este comando.
    npm install nombreDelaDependencia -D 
*/